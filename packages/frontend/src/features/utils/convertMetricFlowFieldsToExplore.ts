import {
    CompiledDimension,
    CompiledMetric,
    DimensionType,
    Explore,
    fieldId,
    FieldType,
    friendlyName,
    MetricType,
    SupportedDbtAdapter,
} from '@lightdash/common';
import { GetMetricFlowFieldsResponse } from '../../api/MetricFlowAPI';

export default function convertMetricFlowFieldsToExplore(
    tableName: string,
    metricFlowFields: GetMetricFlowFieldsResponse,
) {
    const dimensionsMap = metricFlowFields.dimensions.reduce(
        (acc, { name, description, type }) => {
            const dimension: CompiledDimension = {
                fieldType: FieldType.DIMENSION,
                type: type as DimensionType, // ??? what are the types here
                name,
                description,
                label: friendlyName(name),
                table: tableName,
                tableLabel: '',
                sql: '',
                compiledSql: '',
                tablesReferences: [tableName],
                hidden: false,
            };

            return { ...acc, [fieldId(dimension)]: dimension };
        },
        {},
    );
    const metricsMap = metricFlowFields.metricsForDimensions.reduce(
        (acc, { name, description }) => {
            const metric: CompiledMetric = {
                isAutoGenerated: false,
                fieldType: FieldType.METRIC,
                type: MetricType.NUMBER,
                name,
                description,
                label: friendlyName(name),
                table: tableName,
                tableLabel: '',
                sql: '',
                compiledSql: '',
                tablesReferences: [tableName],
                hidden: false,
            };

            return { ...acc, [fieldId(metric)]: metric };
        },
        {},
    );

    const explore: Explore = {
        name: tableName,
        label: '',
        tags: [],
        baseTable: tableName,
        joinedTables: [],
        tables: {
            [tableName]: {
                name: tableName,
                label: '',
                database: '',
                schema: '',
                sqlTable: '',
                dimensions: dimensionsMap,
                metrics: metricsMap,
                lineageGraph: {},
            },
        },
        targetDatabase: SupportedDbtAdapter.POSTGRES,
    };
    return explore;
}
